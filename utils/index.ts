export const SerializeQueryToUrl = function (obj: any) {
  const str = [];
  for (const p in obj)
    if (obj.hasOwnProperty(p)) {
      if (obj[p] !== null) {
        if (obj[p] !== false) {
          if (
            (p === 'storeId' && obj[p] === -1) ||
            (p === 'asc' && obj[p] === true)
          ) {
          } else {
            const value = encodeURIComponent(obj[p]);
            // const value = obj[p];
            if (value.length > 0) {
              str.push(`${encodeURIComponent(p)}=${value}`);
            }
          }
        }
      }
    }
  return str.join('&');
};

